package org.openlca.core.editors.result;

class ResultInfo {

	private String impactMethod;
	private String nwSet;
	private String productSystem;
	private String productFlow;
	private String calculationMethod;

	public String getImpactMethod() {
		return impactMethod;
	}

	public void setImpactMethod(String impactMethod) {
		this.impactMethod = impactMethod;
	}

	public String getNwSet() {
		return nwSet;
	}

	public void setNwSet(String nwSet) {
		this.nwSet = nwSet;
	}

	public String getProductSystem() {
		return productSystem;
	}

	public void setProductSystem(String productSystem) {
		this.productSystem = productSystem;
	}

	public String getProductFlow() {
		return productFlow;
	}

	public void setProductFlow(String productFlow) {
		this.productFlow = productFlow;
	}

	public String getCalculationMethod() {
		return calculationMethod;
	}

	public void setCalculationMethod(String calculationMethod) {
		this.calculationMethod = calculationMethod;
	}

}
